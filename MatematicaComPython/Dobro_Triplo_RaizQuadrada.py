#Ex006 Dobro, Triplo e Raiz Quadrada

print('{:=^40}'.format('Ex006-Dobro, Triplo e Raiz Quadrada'))

numero = int(input('Digite um Valor Inteiro: '))

numero_dobro = numero * 2
numero_triplo = numero * 3
numero_raiz_quadrada = numero**(1/2)

print(f'Dobro de {numero} é = {numero_dobro}')
print(f'Triplo de {numero} é = {numero_triplo}')
print(f'Raiz Quadrada de {numero} = {numero_raiz_quadrada}')